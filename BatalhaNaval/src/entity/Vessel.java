package entity;

public class Vessel {

	private String description;
	private byte lenght;
	
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public byte getTamanho() {
		return lenght;
	}
	public void setTamanho(byte lenght) {
		this.lenght = lenght;
	}
		
}
